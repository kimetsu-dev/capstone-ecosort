// VERSION CONTROL - Increment on each deployment
const CACHE_VERSION = 'v1.2.28'; 

clientsClaim();

// Precache all of the assets generated by your build process.
precacheAndRoute(self.__WB_MANIFEST);

// Set up App Shell-style routing
const fileExtensionRegexp = new RegExp('/[^/?]+\\.[^/]+$');
registerRoute(
  ({ request, url }) => {
    if (request.mode !== 'navigate') {
      return false;
    }
    if (url.pathname.startsWith('/_')) {
      return false;
    }
    if (url.pathname.match(fileExtensionRegexp)) {
      return false;
    }
    return true;
  },
  createHandlerBoundToURL(process.env.PUBLIC_URL + '/index.html')
);

// Cache images
registerRoute(
  ({ url }) => url.origin === self.location.origin && url.pathname.endsWith('.png'),
  new StaleWhileRevalidate({
    cacheName: `images-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({ maxEntries: 50 }),
    ],
  })
);

registerRoute(
  ({ url }) => url.origin === self.location.origin && /\.(jpg|jpeg|gif|svg|webp)$/.test(url.pathname),
  new StaleWhileRevalidate({
    cacheName: `images-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({ maxEntries: 50, maxAgeSeconds: 30 * 24 * 60 * 60 }), // 30 days
    ],
  })
);

// Cache CSS and JS files
registerRoute(
  ({ url }) => url.origin === self.location.origin && /\.(css|js)$/.test(url.pathname),
  new NetworkFirst({
    cacheName: `static-resources-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({ maxEntries: 50 }),
    ],
  })
);

// Cache Google Fonts
registerRoute(
  ({ url }) => url.origin === 'https://fonts.googleapis.com' || url.origin === 'https://fonts.gstatic.com',
  new StaleWhileRevalidate({
    cacheName: `google-fonts-${CACHE_VERSION}`,
    plugins: [
      new ExpirationPlugin({ maxEntries: 20, maxAgeSeconds: 365 * 24 * 60 * 60 }),
    ],
  })
);

// -------------------------------------------------------------------------
// LIFECYCLE EVENTS
// -------------------------------------------------------------------------

// Handle Skip Waiting
self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    console.log('â­ï¸ Skipping waiting phase...');
    self.skipWaiting();
  }
});

// Clean up old caches
self.addEventListener('activate', (event) => {
  console.log(`ðŸ”„ Service Worker ${CACHE_VERSION} activating...`);
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (!cacheName.includes(CACHE_VERSION) && 
              (cacheName.startsWith('images-') || 
               cacheName.startsWith('static-resources-') || 
               cacheName.startsWith('google-fonts-'))) {
            console.log('ðŸ—‘ï¸ Deleting old cache:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    }).then(() => {
      console.log('âœ… Service Worker activated and old caches cleaned');
      return self.clients.claim();
    })
  );
});

self.addEventListener('install', (event) => {
  console.log(`ðŸ“¦ Service Worker ${CACHE_VERSION} installing...`);
  self.skipWaiting();
});

// Optional: Handle Notification Clicks (Open app when notification clicked)
self.addEventListener('notificationclick', function(event) {
  console.log('[Service Worker] Notification click received.');
  
  event.notification.close();

  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then(function(clientList) {
      // If there is already a window open, focus it
      if (clientList.length > 0) {
        let client = clientList[0];
        for (let i = 0; i < clientList.length; i++) {
          if (clientList[i].focused) {
            client = clientList[i];
          }
        }
        
        // Use custom data to decide where to go
        const targetPage = event.notification.data?.target_page || '/';
        
        // If the client is the same origin, navigate to the target page
        if (client.url.startsWith(self.location.origin)) {
            client.navigate(targetPage);
        }
        return client.focus();
      }
      // Otherwise open a new window
      return clients.openWindow('/');
    })
  );
});

// Manual fetch handler (Note: Workbox above also handles fetching. 
// This acts as a fallback or specific handler for current-origin requests).
self.addEventListener('fetch', (event) => {
  if (event.request.url.includes(self.location.origin)) {
    event.respondWith(
      fetch(event.request).then((response) => {
        const clonedResponse = response.clone();
        const headers = new Headers(clonedResponse.headers);
        headers.append('X-SW-Version', CACHE_VERSION);
        return new Response(clonedResponse.body, {
          status: clonedResponse.status,
          statusText: clonedResponse.statusText,
          headers: headers
        });
      }).catch(() => {
        return caches.match(event.request);
      })
    );
  }
});

